function loadLesson() {
  const params = new URLSearchParams(window.location.search);
  const lessonNumber = params.get("lesson");

  if (lessons[lessonNumber]) {
    document.getElementById("lesson-title").innerHTML = lessons[lessonNumber].title;
    document.getElementById("lesson-content").innerHTML = lessons[lessonNumber].content;
  }
}

if (window.location.pathname.includes("lesson.html")) {
  loadLesson();
}